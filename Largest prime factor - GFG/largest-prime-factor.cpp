class Solution{
public: 
    long long int largestPrimeFactor(int n){
        // code here
        long long int ans = -1;
        int i = 2;
        while(n>1){
            while(n%i==0){
                n = n/i;
                ans = i;
            }
            i++;
        }
        return ans;
    }
};

